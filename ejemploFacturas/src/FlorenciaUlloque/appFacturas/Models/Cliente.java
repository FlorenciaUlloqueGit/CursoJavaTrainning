package FlorenciaUlloque.appFacturas.Models;

public class Cliente {
    private String nombre;
    private String identfiscal;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getIdentfiscal() {
        return identfiscal;
    }

    public void setIdentfiscal(String identfiscal) {
        this.identfiscal = identfiscal;
    }
}
